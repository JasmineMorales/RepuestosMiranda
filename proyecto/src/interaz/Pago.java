    private void Saldo(){
        float Total = 0;
        Total += Float.parseFloat(txt_CreditoCant.getText());
        Total += Float.parseFloat(txt_Efectivo.getText());
        Total += Float.parseFloat(txt_Cheque1cant.getText());
        Total += Float.parseFloat(txt_Cheque2cant.getText()); 
        Total += Float.parseFloat(txt_Cheque3cant.getText());
        Total += Float.parseFloat(txt_OtrosCant.getText());
        txt_Total.setValue(Total);
        txt_Saldo.setValue(TotalFac - Total);
        btn_Aceptar2.setEnabled((TotalFac - Total) == 0);
   }

