    private void okey(){
         try {
             Saldo();
             if (txt_Saldo.getText().equals("0.00")) {
                if (Conexion_DB.login(Conexion_DB.obtenerUsuario(Trabajador_id), new String(jPasswordField1.getPassword())) == 1) {
                    credito = new Float(txt_CreditoCant.getText());
                    efectivo = new Float(txt_Efectivo.getText());
                    cheque2 = new Float(txt_Cheque2cant.getText());
                    cheque3 = new Float(txt_Cheque3cant.getText());
                    cheque1 = new Float(txt_Cheque1cant.getText());
                    otros = new Float(txt_OtrosCant.getText());
                    desCheque1 = (txt_Cheque1Coment.getText().equals("INGRESE INFROMACION DEL CHEQUE"))? "": txt_Cheque1Coment.getText();
                    desCheque2 = (txt_Cheque2Coment.getText().equals("INGRESE INFROMACION DEL CHEQUE"))? "": txt_Cheque2Coment.getText();
                    desCheque3 = (txt_Cheque3Coment.getText().equals("INGRESE INFROMACION DEL CHEQUE"))? "": txt_Cheque3Coment.getText();
                    desOtros = (txt_OtroComent.getText().equals("INGRESE INFROMACION DEL CHEQUE"))? "": txt_OtroComent.getText();
                    Seleccionado = true;
                    this.setVisible(false);
                }else{
                    DialogoOpcion dialogo = new DialogoOpcion(null, true, DialogoOpcion.ICONO_ERROR, "ERROR", "CLAVE INCORRECTA");
                    dialogo.setVisible(true);
                    Seleccionado = false;
                }
             }else{
                    DialogoOpcion dialogo = new DialogoOpcion(null, true, DialogoOpcion.ICONO_ERROR, "ERROR", "EL SALDO DEBE SER IGUAL A 0"
                            + " \nLA SUMA DE LOS METODOS DE PAGO DEBER SER IGUAL AL TOTAL DE LA FACTURA");
                    dialogo.setVisible(true);
                    Seleccionado = false;
             }
            } catch (NoSePuedeConectar | SQLException ex) {
                DialogoOpcion dialogo = new DialogoOpcion(null, true, DialogoOpcion.ICONO_ERROR, "ERROR", ex.getMessage());
                dialogo.setVisible(true);
            }
    }
